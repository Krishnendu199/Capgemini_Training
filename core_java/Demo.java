package core_java;

public class Demo {
	public static void main(String[] args) {
		char c = 'A';
		int a = c;
		System.out.println(a);
		
		int b = 97;
		char c2 = (char)b;
		System.out.println(c2);
		
		float f = 3.34f;
		byte b2 = (byte)f;
		System.out.println(b2);
		
		byte b3 = 20;
		float f2 = (float)b3;
		System.out.println(f2);
		
	}

}
